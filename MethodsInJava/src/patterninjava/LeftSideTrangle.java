package patterninjava;

public class LeftSideTrangle {
	public static void main(String[] args) {
		System.out.println("left side trangle:");
		for(int i=1;i<=5;i++) {		//loop i 
			for(int j=1;j<=i;j++) {	//loop j
				System.out.print("*");
				}	//close loop j
		System.out.println(" ");
	} 	//close loop i
	
	}
}
