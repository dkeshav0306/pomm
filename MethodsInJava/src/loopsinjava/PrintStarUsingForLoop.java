package loopsinjava;

public class PrintStarUsingForLoop {
public static void main(String[] args) {
	System.out.println("Right Trangle downword Star Pattern using increment operator:-");
	for(int i=1;i<=5;i++) {
		for(int j=i;j<=5;j++) {
			System.out.print("*");
		}
		System.out.println("");
	}
}
}
